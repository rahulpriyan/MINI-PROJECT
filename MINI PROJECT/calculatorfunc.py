def add(a,b):
    return a+b
def subtract(a,b):
    return a-b
def multiply(a,b):
    return a*b
def division(a,b):
    return a//b
def modules(a,b):
    return a%b
def floor_division(a,b):
    return a//b
print("addition:",add(10,5))
print("subtraction:",subtract(10,7))
print("multiplication:",multiply(10,6))
print("division:",division(90,7))
print("modules:",modules(10,5))
print("floor_division:",floor_division(10,7))
